            <h2 data-aos="fade-down" data-aos-duration="1000">Logic Powered Solutions <br> Cyber Security Policy</h2>
            <div data-aos="zoom-in" data-aos-duration="1000" class="securitymore-content">
                <h3>Policy Brief & Purpose</h3>
                <p>Our cyber security policy outlines our guidelines and provisions for preserving the security of our
                    data and technology infrastructure.</p>
                <p>The more we rely on technology to collect, store and manage information, the more vulnerable we
                    become to severe security breaches. Human errors, hacker attacks and system malfunctions could cause
                    great financial damage and may jeopardize our company’s reputation.</p>
                <p>For this reason, we have implemented a number of security measures. We have also prepared
                    instructions that may help mitigate security risks. We have outlined both provisions in this policy.
                </p>
            </div>
            <div class="securitymore-content">
                <h3>Scope</h3>
                <p>This policy applies to all our employees, contractors, volunteers and anyone who has permanent or
                    temporary access to our systems and hardware.</p>
                <ul>
                    <li>Policy Elements</li>
                    <li>Confidential Data</li>
                    <li>Confidential Data is Secret and Valuable</li>
                    <li>Unpublished Financial Information</li>
                    <li>Data of Customers/Partners/Vendors</li>
                    <li>Patents, Formulas or new Technologies</li>
                    <li>Customer Lists (Existing and Prospective)</li>
                </ul>
                <p>All employees are obliged to protect this data. In this policy, we will give our employees
                    instructions on how to avoid security breaches.</p>
            </div>
            <div class="securitymore-content">
                <h3>Protect Personal and Company Devices</h3>
                <p>When employees use their digital devices to access company emails or accounts, they introduce
                    security risk to our data. We advise our employees to keep both their personal and company-issued
                    computer, tablet and cell phone secure. They can do this if they:</p>
                <ul>
                    <li>Keep all devices password protected</li>
                    <li>Choose and upgrade a complete antivirus software</li>
                    <li>Ensure they do not leave their devices exposed or unattended</li>
                    <li>Install security updates of browsers and systems monthly or as soon as updates are available
                    </li>
                    <li>Log into company accounts and systems through secure and private networks only</li>
                    <li>We also advise our employees to avoid accessing internal systems and accounts from other
                        people’s devices or lending their own devices to others</li>
                </ul>
                <p>When new hires receive company-issued equipment they will receive instructions for:</p>
                <ul>
                    <li>Disk encryption setup</li>
                    <li>Password management tool setup</li>
                    <li>Installation of antivirus/ anti-malware software</li>
                </ul>
                <p>They should follow instructions to protect their devices and refer to our [Security Specialists/
                    Network Engineers] if they have any questions.</p>
            </div>
            <div class="securitymore-content">
                <h3>Keep Emails Safe</h3>
                <p>Emails often host scams and malicious software (e.g. worms.) To avoid virus infection or data theft,
                    we instruct employees to:</p>
                <ul>
                    <li>Avoid opening attachments and clicking on links when the content is not adequately explained
                        (e.g. “watch this video, it’s amazing.”)</li>
                    <li>Be suspicious of clickbait titles (e.g. offering prizes, advice.)</li>
                    <li>Check email and names of people they received a message from to ensure they are legitimate.</li>
                    <li>Look for inconsistencies or give-aways (e.g. grammar mistakes, capital letters, excessive number
                        of exclamation marks.)</li>
                    <li>If an employee isn’t sure that an email they received is safe, they can refer to our [IT
                        Specialist.]</li>
                </ul>
            </div>
            <div class="securitymore-content">
                <h3>Manage Passwords Properly</h3>
                <p>Password leaks are dangerous since they can compromise our entire infrastructure. Not only should
                    passwords be secure so they won’t be easily hacked, but they should also remain secret. For this
                    reason, we advice our employees to:</p>
                <ul>
                    <li>Choose passwords with at least eight characters (including capital and lower-case letters,
                        numbers and symbols) and avoid information that can be easily guessed (e.g. birthdays.)</li>
                    <li>Remember passwords instead of writing them down. If employees need to write their passwords,
                        they are obliged to keep the paper or digital document confidential and destroy it when their
                        work is done.</li>
                    <li>Exchange credentials only when absolutely necessary. When exchanging them in-person isn’t
                        possible, employees should prefer the phone instead of email, and only if they personally
                        recognize the person they are talking to.</li>
                </ul>
            </div>
            <div class="securitymore-content">
                <h3>Change Their Passwords Every Two Months</h3>
                <p>Remembering a large number of passwords can be daunting. We will purchase the services of a password
                    management tool which generates and stores passwords. Employees are obliged to create a secure
                    password for the tool itself, following the abovementioned advice.</p>
            </div>
            <div class="securitymore-content">
                <h3>Transfer Data Securely</h3>
                <p>Transferring data introduces security risk. Employees must:</p>
                <ul>
                    <li>Avoid transferring sensitive data (e.g. customer information, employee records) to other devices
                        or accounts unless absolutely necessary. When mass transfer of such data is needed, we request
                        employees to ask our [Security Specialists] for help.</li>
                    <li>Share confidential data over the company network/ system and not over public Wi-Fi or private
                        connection.</li>
                    <li>Ensure that the recipients of the data are properly authorized people or organizations and have
                        adequate security policies.</li>
                    <li>Report scams, privacy breaches and hacking attempts.</li>
                    <li>Our [IT Specialists/ Network Engineers] need to know about scams, breaches and malware so they
                        can better protect our infrastructure. For this reason, we advise our employees to report
                        perceived attacks, suspicious emails or phishing attempts as soon as possible to our
                        specialists. Our [IT Specialists/ Network Engineers] must investigate promptly, resolve the
                        issue and send a companywide alert when necessary.</li>
                    <li>Our Security Specialists are responsible for advising employees on how to detect scam emails. We
                        encourage our employees to reach out to them with any questions or concerns.</li>
                </ul>
            </div>
            <div class="securitymore-content">
                <h3>Additional Measures</h3>
                <p>To reduce the likelihood of security breaches, we also instruct our employees to:</p>
                <ul>
                    <li>Turn off their screens and lock their devices when leaving their desks.</li>
                    <li>Report stolen or damaged equipment as soon as possible to [HR/ IT Department].</li>
                    <li>Change all account passwords at once when a device is stolen.</li>
                    <li>Report a perceived threat or possible security weakness in company systems.</li>
                    <li>Refrain from downloading suspicious, unauthorized or illegal software on their company
                        equipment.</li>
                    <li>Avoid accessing suspicious websites.</li>
                    <li>We also expect our employees to comply with our social media and internet usage policy.</li>
                </ul>
            </div>
            <div class="securitymore-content">
                <h3>Our [Security Specialists/ Network Administrators] Should:</h3>
                <ul>
                    <li>Install firewalls, anti-malware software and access authentication systems.</li>
                    <li>Arrange for security training to all employees.</li>
                    <li>Inform employees regularly about new scam emails or viruses and ways to combat them.</li>
                    <li>Investigate security breaches thoroughly.</li>
                    <li>Follow this policies provisions as other employees do.</li>
                    <li>Our company will have all physical and digital shields to protect information.</li>
                </ul>
            </div>
            <div class="securitymore-content">
                <h3>Remote Employees</h3>
                <p>Remote employees must follow this policy’s instructions too. Since they will be accessing our
                    company’s accounts and systems from a distance, they are obliged to follow all data encryption,
                    protection standards and settings, and ensure their private network is secure.</p>
                <p>We encourage them to seek advice from our [Security Specialists/ IT Administrators.]</p>
            </div>
            <div class="securitymore-content">
                <h3>Disciplinary Action</h3>
                <p>We expect all our employees to always follow this policy and those who cause security breaches may
                    face disciplinary action:</p>
                <ul>
                    <li>First-time, unintentional, small-scale security breach: We may issue a verbal warning and train
                        the employee on security.</li>
                    <li>Intentional, repeated or large-scale breaches (which cause severe financial or other damage): We
                        will invoke more severe disciplinary action up to and including termination.</li>
                    <li>We will examine each incident on a case-by-case basis.</li>
                    <li>Additionally, employees who are observed to disregard our security instructions will face
                        progressive discipline, even if their behavior hasn’t resulted in a security breach.</li>
                </ul>
            </div>
            <div class="securitymore-content">
                <h3>Take Security Seriously</h3>
                <p>Everyone, from our customers and partners to our employees and contractors, should feel that their
                    data is safe. The only way to gain their trust is to proactively protect our systems and databases.
                    We can all contribute to this by being vigilant and keeping cyber security top of mind.</p>
